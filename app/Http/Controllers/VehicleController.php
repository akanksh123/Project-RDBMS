     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $vehicle=new Vehicle;
        $vehicle->aadharno=$request->input('aadharno');
        $vehicle->engineno=$request->input('engineno');
        $vehicle->bname=$request->input('bname');
        $vehicle->vtype=$request->input('vtype');
        $vehicle->vcompany=$request->input('vcompany');
        $vehicle->vmodel=$request->input('vmodel');
        
        $vehicle->save();
        return redirect('/page')->with('success','Vehicle Registration Successful');
    }

